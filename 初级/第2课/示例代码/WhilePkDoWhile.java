
public class WhilePkDoWhile {
    public static void main(String[] args) {
        // While循环
        int num = 50;
        while (num < 50) {
            System.out.println("While循环被执行了");
        }
        // Do-While循环
        do {
            System.out.println("Do-While循环被执行了");
        } while (num < 50);
    }
}
